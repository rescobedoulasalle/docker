#!/usr/bin/perl 

# Perl program to illustrate 
# the use of numbers 

# Integer 
$a = 20; 

# Floating Number 
$b = 20.5647; 

# Scientific value 
$c = 123.5e-10; 

# Hexadecimal Number 
$d = 0xc; 

# Octal Number 
$e = 074; 

# Binary Number 
$f = 0b1010; 

# Printing these values 
print("Integer: ", $a, "\n"); 
print("Float Number: ", $b, "\n"); 
print("Scientific Number: ", $c, "\n"); 
print("Hex Number: ", $d, "\n"); 
print("Octal number: ", $e, "\n"); 
print("Binary Number: ", $f, "\n"); 

